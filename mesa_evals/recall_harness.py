"""MESA v0.6.1 — B-4: End-to-End Recall@5 Evaluation Harness.

Validates the full ingestion→search pipeline by:
  1. Loading ``legal_golden.json`` (generated by ``legal_generator.py``).
  2. Async POSTing all entries to ``/v3/memory/insert``.
  3. Polling the API/SQLite until all rows reach ``processed`` status.
  4. Issuing search queries for each entry via ``/v3/memory/search``.
  5. Computing Recall@5 by comparing retrieved ``entity_name`` values
     against each entry's ``expected_triplets``.
  6. Outputting a strict JSON metrics report.

Usage::

    # Requires a running MESA server on localhost:8000
    python -m mesa_evals.recall_harness --golden legal_golden.json
    python -m mesa_evals.recall_harness --golden legal_golden.json --base-url http://localhost:8001
    python -m mesa_evals.recall_harness --golden legal_golden.json --api-key MY_KEY
"""

