# Main orchestrator
# ---------------------------------------------------------------------------


async def run_harness(
    *,
    golden_path: str,
    base_url: str,
    api_key: str,
    agent_id: str,
    session_id: str,
    k: int,
    concurrency: int,
    poll_interval: float,
    poll_timeout: float,
) -> HarnessReport:
    """Execute the full Recall@K evaluation pipeline."""
    report = HarnessReport(golden_path=golden_path, k=k)
    t_total_start = time.monotonic()

    # Step 1: Load golden dataset
    entries = load_golden_dataset(golden_path)
    report.total_entries = len(entries)

    connector = aiohttp.TCPConnector(
        limit=concurrency,
        enable_cleanup_closed=True,
    )
    async with aiohttp.ClientSession(connector=connector) as session:
        # Step 2: Ingest all entries
        logger.info("INGEST | Starting ingestion of %d entries...", len(entries))
        ingest_results = await ingest_entries(
            session,
            entries,
            base_url=base_url,
            agent_id=agent_id,
            session_id=session_id,
            api_key=api_key,
            concurrency=concurrency,
        )

        successful_ids = []
        for ir in ingest_results:
            if ir["status"] == "queued" and ir.get("log_id") is not None:
                report.ingested += 1
                successful_ids.append(ir["log_id"])
            elif ir["status"] == "error":
                report.errors.append(
                    {
                        "phase": "ingest",
                        "entry_id": ir["entry_id"],
                        "error": ir.get("error", "unknown"),
                    }
                )

        logger.info(
            "INGEST | Done: %d/%d queued, %d errors",
            report.ingested,
            report.total_entries,
            len(report.errors),
        )

        # Step 3: Poll until processed
        logger.info("POLL | Waiting for cold-path processing...")
        report.processed = await poll_until_processed(
            session,
            successful_ids,
            base_url=base_url,
            api_key=api_key,
            poll_interval=poll_interval,
            poll_timeout=poll_timeout,
        )

        # Step 4 & 5: Search and evaluate Recall@K
        logger.info(
            "SEARCH | Evaluating Recall@%d for %d entries...",
            k,
            len(entries),
        )
        entry_results, latencies = await evaluate_recall(
            session,
            entries,
            base_url=base_url,
            agent_id=agent_id,
            session_id=session_id,
            api_key=api_key,
            k=k,
            concurrency=concurrency,
        )

    report.entry_results = entry_results
    report.searched = len(entry_results)
    report.elapsed_total_s = time.monotonic() - t_total_start

    # Compute aggregate metrics
    if entry_results:
        n = len(entry_results)
        report.recall_at_k = sum(er.recall_at_k for er in entry_results) / n
        report.hit_rate = sum(1.0 if er.hit else 0.0 for er in entry_results) / n
        report.proxy_faithfulness = (
            sum(er.proxy_faithfulness for er in entry_results) / n
        )
        report.context_precision = sum(er.context_precision for er in entry_results) / n
        report.answer_relevance = sum(er.answer_relevance for er in entry_results) / n

    if latencies:
        report.mean_latency_ms = sum(latencies) / len(latencies)

    return report


