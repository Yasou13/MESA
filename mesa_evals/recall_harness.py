def main() -> None:
    """CLI entrypoint for the Recall@K evaluation harness."""
    parser = argparse.ArgumentParser(
        description="MESA v0.6.1 — B-4: End-to-End Recall@K Harness",
    )
    parser.add_argument(
        "--golden",
        type=str,
        default=DEFAULT_GOLDEN_PATH,
        help=f"Path to legal_golden.json (default: {DEFAULT_GOLDEN_PATH})",
    )
    parser.add_argument(
        "--adapter",
        type=str,
        default="mesa",
        choices=["mesa", "mem0", "barerag", "letta", "zep"],
        help="Adapter to use for benchmark (default: mesa)",
    )
    parser.add_argument(
        "--agent-id",
        type=str,
        default=DEFAULT_AGENT_ID,
        help=f"Agent ID for scoped ingestion (default: {DEFAULT_AGENT_ID})",
    )
    parser.add_argument(
        "--k",
        type=int,
        default=DEFAULT_K,
        help=f"K for Recall@K (default: {DEFAULT_K})",
    )
    parser.add_argument(
        "--concurrency",
        type=int,
        default=DEFAULT_CONCURRENCY,
        help=f"Max concurrent operations (default: {DEFAULT_CONCURRENCY})",
    )
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Write JSON report to file (default: stdout)",
    )
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(name)s] %(levelname)s: %(message)s",
        datefmt="%H:%M:%S",
    )

    report = asyncio.run(
        run_harness(
            golden_path=args.golden,
            adapter_name=args.adapter,
            agent_id=args.agent_id,
            k=args.k,
            concurrency=args.concurrency,
        )
    )

    report_json = report.to_json()

    if args.output:
        with open(args.output, "w", encoding="utf-8") as f:
            json.dump(report_json, f, ensure_ascii=False, indent=2)
        logger.info("REPORT | Written to %s", args.output)
    else:
        print(json.dumps(report_json, ensure_ascii=False, indent=2))

    # Exit code: 0 if recall > 0, 1 otherwise
    if report.recall_at_k > 0:
        logger.info(
            "HARNESS_PASS | Recall@%d = %.4f | Hit Rate = %.4f | "
            "Faithfulness = %.4f | Ctx Precision = %.4f | Ans Relevance = %.4f",
            report.k,
            report.recall_at_k,
            report.hit_rate,
            report.proxy_faithfulness,
            report.context_precision,
            report.answer_relevance,
        )
    else:
        logger.warning(
            "HARNESS_WARN | Recall@%d = 0.0 | "
            "Faithfulness = %.4f | Ctx Precision = %.4f | Ans Relevance = %.4f",
            report.k,
            report.proxy_faithfulness,
            report.context_precision,
            report.answer_relevance,
        )


if __name__ == "__main__":
    main()
