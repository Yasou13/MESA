import random
import time
from typing import Any, Dict

from ..datasets.schemas import BenchmarkQuestion, MemoryContext
from .base import AbstractBenchmarkClient, BenchmarkResponse


class DummyClientAdapter(AbstractBenchmarkClient):
    """
    A dummy client for testing the benchmark pipeline without connecting to a real system.
    """

    def __init__(self) -> None:
        self.verbose = False
        self.ingested_memory_count = 0

    def initialize(self, config_params: Dict[str, Any]) -> None:
        self.verbose = config_params.get("verbose", False)
        if self.verbose:
            print("[DummyClient] Initialized with config:", config_params)

    def clear_memory(self) -> None:
        self.ingested_memory_count = 0
        if self.verbose:
            print("[DummyClient] Memory cleared.")

    def add_memory(self, context: MemoryContext) -> Dict[str, Any]:
        start_time = time.time()

        # Simulate network/processing delay
        time.sleep(random.uniform(0.01, 0.05))

        self.ingested_memory_count += 1
        latency = (time.time() - start_time) * 1000

        if self.verbose:
            print(
                f"[DummyClient] Ingested memory '{context.id}'. Latency: {latency:.2f}ms"
            )

        return {"latency_ms": latency}

    def answer(self, question: BenchmarkQuestion) -> BenchmarkResponse:
        start_time = time.time()

        # Simulate network/processing delay (retrieval is usually slower)
        time.sleep(random.uniform(0.05, 0.2))

        latency = (time.time() - start_time) * 1000

        # Randomly decide to "retrieve" one of the expected contexts if any exist
        retrieved = []
        if question.expected_context_ids and random.random() > 0.5:
            retrieved.append(random.choice(question.expected_context_ids))

        response = BenchmarkResponse(
            answer_text="This is a dummy answer generated by the test adapter.",
            retrieved_context_ids=retrieved,
            latency_ms=latency,
            token_usage={"prompt": 150, "completion": 25},
            metadata={"dummy": True},
        )

        if self.verbose:
            print(
                f"[DummyClient] Answered '{question.id}'. Latency: {latency:.2f}ms. Retrieved: {retrieved}"
            )

        return response

    def close(self) -> None:
        """Dummy client requires no cleanup."""
        pass
